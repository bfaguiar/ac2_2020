#include <detpic32.h>

int main() 
{
	char display7SCodes[] = {0x3F, 0x06, 0x5B, 0x4F, 0x66, 0x6D, 0x7D, 0x07, 0x7F, 0x6F, 0x77, 0x7C, 0x39, 0x5E, 0x79, 0x71};

	LATB = LATB & 0x80FF;
	LATDbits.LATD5 = 1;
	LATDbits.LATD6 = 0;

	TRISB = (TRISB & 0x80FF) | 0x000F;
	TRISDbits.TRISD5 = 0;
	TRISDbits.TRISD6 = 0;

	while(1)
	{
		LATB = (LATB & 0x00FF) | (display7SCodes[(PORTB & 0x000F)] << 8);
	}
}